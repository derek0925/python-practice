from math import *
#如何使用數字的用法
number = -8 
print( 8 + number)
print("會印出數字" + str(number))

#abs絕對值
print(abs(number))
#pow 次方
print(pow(2,0))
#max 最大值
print(max(100,11,22,33,44,55))
#min 最小值
print(min(100,11,22,33,44,55))
#round 四捨五入
print(round(4.564854545))
#floor 無條件捨去
print(floor(3.564854545))
#無條件進位 ceil
print(ceil(3.564854545))
#sqrt開根號
print(sqrt(49))